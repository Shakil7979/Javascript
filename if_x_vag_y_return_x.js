

// একটি ফাংশন লিখুন যা দুটি সংখ্যা নেয়, বলুন x এবং y, যুক্তি হিসাবে
// x কে y দ্বারা বিভাজ্য কিনা তা পরীক্ষা করুন
// যদি হ্যাঁ হয়, x ফেরত দিন
// যদি না হয়, তাহলে পরবর্তী উচ্চতর প্রাকৃতিক সংখ্যাটি ফেরত দিন যা y দ্বারা বিভাজ্য 

function myFunction(x, y) {
  while (x % y !== 0) {
    x++;
  }
  return x;
}

console.log(myFunction(33, 20));


